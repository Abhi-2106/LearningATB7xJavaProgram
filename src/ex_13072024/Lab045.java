package ex_13072024;

public class Lab045 {
    public static void main(String[] args) {
        System.out.println(10==10); // == to compare the 2 values
        System.out.println(10>=10); // 10 >10 OR 10 =10
        System.out.println(10<=10); // 10<10  OR  10 =10
        System.out.println(10>10);  // 10> 10
        System.out.println(10<10);  // 10<10
    }
}
